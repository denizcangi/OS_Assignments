public class Philosopher extends Thread {

	private static Table table;

	private int THINKING = 0;
	private int HUNGRY = 1;
	private int EATING = 2;


	private Semaphore [] sem;
	private Semaphore[] barriers;
	private Semaphore mutex;
	private int id;

	private int [] state;
	private int N;

	public Philosopher(int i, Semaphore [] s, int [] mystate, int philnumber, Semaphore mu, Semaphore[] bar)
	{
		id = i;
		sem = s;
		state = mystate;
		N = philnumber;
		mutex = mu;
		barriers = bar;
	}

	public static void main(String args[]) throws InterruptedException {

		EventQueue.invokeLater(new Runnable() {
			public void run() {
				try {
					table = new Table();
					table.frame.setVisible(true);

				}
				catch(Exception e){
					e.printStackTrace();

				}
			}
		});

		int N = 5;
		int [] state = new int [N];


		Semaphore mutex = new Semaphore (1);
		Semaphore [] semarray = new Semaphore [N];
		Semaphore [] bararray = new Semaphore [N];

		Philosopher[] oldies = new Philosopher [N];

		for (int i=0; i < N; i++)
		{
			semarray[i] = new Semaphore(0,true);
			bararray[i] = new Semaphore(0,true);
		}
		for (int i=0; i < N; i++)
		{
			oldies[i] = new Philosopher(i, semarray, state, N, mutex,bararray);
			oldies[i].start();
			try {
				sleep(5000);
			} catch (InterruptedException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
		}

	}

	public void walkRandomly(){ //philosophers walks random amount of time between 1 to 10 seconds(1000 to 10000 millisecond) towards to table

		Random random = new Random();
		int rand;
		while(true){
			rand=random.nextInt(10001); //create a rand int between 1000 to 10000 milliseconds and thread sleep for that amount of time
			if (rand >= 1000) break;
		}
		try {
			sleep(rand);
		} catch (InterruptedException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

	public void implementBarrier(){ //this function implements barrier to start dining

		for (int a=0; a<N; a++){
			if(a!=id){
				barriers[a].release(); //firstly every thread do up on the other threads' barriers
			}
		}

		for(int k=0; k<N-1;k++){ //then they do down to their own barriers for N-1 time where N is number of threads(philosophers)
			try {
				barriers[id].acquire();
			} catch (InterruptedException e) {
				e.printStackTrace();
			}
		}
	}

	public void take_forks() throws InterruptedException { //this function is to take the forks

		mutex.acquire(); //try lock the mutex
		state[id]=HUNGRY; //state become hungry
		table.Hungry_GUI(id); //make the plate hungry state
		test(id); //philosopher test if she can take the forks with testing if the neighbor philosophers are eating
		mutex.release(); //unlock the mutex
		sem[id].acquire(); //block the semaphore if philosopher couldn't eat
	}

	public void put_forks() throws InterruptedException {
		mutex.acquire(); //try lock the mutex
		table.ForkPut_GUI(id); //free the forks
		state[id]=THINKING; //philosopher go back to thinking state
		test((id+N-1)%N); //test if the left philosopher can eat, if she can then she start eating
		test((id+1)%N); //test if the right philosopher can eat if she can, she start eating
		mutex.release(); //unlock the mutex
	}

	public void test(int i) throws InterruptedException {

		if (state[i]==HUNGRY && state[(i+N-1)%N]!=EATING && state[(i+1)%N]!=EATING){ //if the state of the i'th philosopher is hungry and left and right neighbors are not eating
			table.ForkTake_GUI(i); //take the Fork from table
			state[i]=EATING; //state of the philosopher becomes Eating
			sem[i].release(); //up on semaphore of the philosopher
		}
	}

	public void EatingState(){ //eating state of the philosopher
		table.Eating_GUI(id);
		table.StopEating_GUI(id);
	}

	public void ThinkState(){ //thinking random amount of time between 0 to 10 seconds(0 to 10000 milliseconds)
		Random random = new Random();
		int rand = random.nextInt(10001);

		try {
			sleep(rand);
		} catch (InterruptedException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}
	public void run() {

		walkRandomly();
		table.PutPlate_GUI(id);
		implementBarrier();
		table.StartDining_GUI(id);
		state[id]=THINKING; //first the state of every philosopher is thinking state

		while(true){ //this loop run forever if not interrupted

			ThinkState(); //think for random amount of time, plate is white-black
			try {
				take_forks(); //try to take forks, hungry state, plate is red
			} catch (InterruptedException e) {
				e.printStackTrace();
			}
			EatingState(); //plate turns to blue while eating
			try {
				put_forks(); //release the forks
			} catch (InterruptedException e) {
				e.printStackTrace();
			}
		}

		// TODO Auto-generated method stub		
	}

}







